package com.syntax.class07;

public class Hw4 {

	public static void main(String[] args) {
		//  Print odd numbers between 20 and 50 (2 ways)
		int o=21;
		while(o<=50) {
			System.out.print(o+" ");
			o+=2;
			
		}
		
		System.out.println("------------------------");
		for(int a=21; a<=50; a+=2 ) {
			System.out.print(a+" ");
		}
		
		}
	}


