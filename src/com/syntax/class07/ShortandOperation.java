package com.syntax.class07;

public class ShortandOperation {

	public static void main(String[] args) {
		
		
		
		int num=90;
		System.out.println(num);  //19
		// another way to increment the variable
		num++;//+1
		System.out.println(num);  //92
		// to decrement the variable
		num--;
		System.out.println(num);  //91
		//----------------------------------
		int i=10;
	        i=i+9;
	        System.out.println(i);  //19

	        i=i-5;
	        System.out.println(i);  //14


	        i=12;
	        System.out.println(i);  //28

	        int a=10;

	        a+=9;
	        System.out.println(a);  //19

	        a-=5;
	        System.out.println(a);  //14

	        a=2;
	        System.out.println(a);  //28

	        a-=10;
	        System.out.println(a);  //18

	        a%=4;
	        System.out.println("Value after mod operation=" +a);  //Value after mode operation=2

	        a*=10;
	        System.out.println(a);  //20

	        a+=1;
	        System.out.println(a);  //21




	}

}
