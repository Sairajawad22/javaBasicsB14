package com.syntax.class07;

public class Hw2 {

	public static void main(String[] args) {
		//  Print numbers from 100 to 1
		int i=100;
		while(i>=1) {
			System.out.print(i+ " ");
			i--;
	}
		System.out.println("------------------------ ");
		for(int b=100; b>=1; b--) {
			System.out.print(b+ " ");
		}
	}
}
