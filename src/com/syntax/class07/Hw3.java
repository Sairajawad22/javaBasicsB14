package com.syntax.class07;

public class Hw3 {

	public static void main(String[] args) {
		//  Print even numbers from 20 to 1 (2 ways)
// first way
		for(int d=20; d>=1; d-=2) {// we are substracting 2 from d to get even num
			System.out.print(d+" ");
				
		}
		System.out.println("-----------------------------");
		// second way
		for(int a=20; a>=1; a-=2) {
			if(a%2==0) {// it means if the result is 0 print the statment otherwise don't print
				System.out.print(a+" ");
				
			}
			
			
	}
		}
	}

