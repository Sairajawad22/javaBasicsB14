package com.syntax.class03;

public class RecapOpertors {

	public static void main(String[] args) {
		/*
		 * addition=> +
		 * subtraction=> -
		 * multiplication=> *
		 * modulus=>%
		 */
		int number1=11;// +3 +3 +3 = 2
		int number2=3;
		System.out.println("Answer=" +number1/number2);// dividing number1 by number2 
		System.out.println("Reminder="+number1%number2);
		double number3=9;
		double number4=2;
		System.out.println(number3/number4);
		
		float number5=1.0f;
		float number6=3.0f;
		System.out.println(number5/number6);
		
		double number7=1;
		double number8=3;
		System.out.println(number7/number8);
		
		
	}

}
